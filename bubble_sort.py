# 18. Python Program for Bubble Sort
def bubble_sort(lst):
    swapped = False

    for i in range(len(lst) - 1):
        for j in range(0, len(lst) - i - 1):

            if lst[j] > lst[j + 1]:
                swapped = True
                lst[j], lst[j + 1] = lst[j + 1], lst[j]
            # print(lst)
        if not swapped:
            return


lst = [18, 12, 8, 25, 35, 6, 10, 21]
bubble_sort(lst)
print('Sorted using bubble sort: ', lst)

